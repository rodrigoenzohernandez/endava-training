/**
 * It's time to practice returning values from a function! Write a simple function multiply which
accepts two numerical arguments and returns their product (multiply them together). Make
sure to return the value instead of printing it!
        multiply(2,3) // 6
        multiply(9,9) // 81
        multiply(5,4) // 20
 */

function multiply(numberA, numberB) {
    return numberA * numberB
}

console.log(multiply(2,3)); //6
console.log(multiply(9,9)); //81
console.log(multiply(5,4)); //20
